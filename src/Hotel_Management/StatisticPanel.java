    private void monthKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_monthKeyTyped
        // TODO add your handling code here:
        try
        {
            String query = "select * from thanhtoan where month(ngtt) ='"+month.getText()+"'";
            Connection conn =  new DataConnection().Connect();
            Statement stat = conn.createStatement();
            ResultSet rs = stat.executeQuery(query);
            

            while (rs.next())
            {
                String matt = String.valueOf(rs.getInt("matt"));
                String makh = String.valueOf(rs.getInt("makh"));
                String maphong = String.valueOf(rs.getInt("maphong"));
                String ngtt = rs.getString("ngtt");
                String thanhtien = String.valueOf(rs.getInt("thanhtien"));
                
                String tbData[] = {matt,makh,maphong,ngtt,thanhtien};
                
                DefaultTableModel tblModel;
                tblModel = (DefaultTableModel)DoanhThutb.getModel();
                tblModel.addRow(tbData);
                
                int sum=0;
                for (int i=0;i<tblModel.getRowCount();i++)
                {
                    sum += Integer.parseInt(tblModel.getValueAt(i, 4)+"");
                }
                //System.out.println(sum);
                SumOf.setText(Integer.toString(sum));
            }

        }
        catch(Exception e)
        {
            System.out.println(e);
            System.out.println("Lỗi!");
        }
        
        
    }//GEN-LAST:event_monthKeyTyped

    private void printMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_printMouseClicked
        // TODO add your handling code here:
        String path = "";
        JFileChooser j = new JFileChooser();
        j.setFileSelectionMode(JFileChooser.DIRECTORIES_ONLY);
        int x= j.showSaveDialog(this);
        
        SimpleDateFormat formatter = new SimpleDateFormat("dd/MM/yyyy");
        SimpleDateFormat codeformat = new SimpleDateFormat("ssmmHHddMMyyyy");
        java.util.Date now = new java.util.Date();
        
        if (x==JFileChooser.APPROVE_OPTION)
        {
            path = j.getSelectedFile().getPath();
        }
        
        Document doc = new Document();
        
        try
        {
            PdfWriter.getInstance(doc, new FileOutputStream(path+"/DoanhThu"+ Math.random() +".pdf"));
            doc.open();
            
            doc.add(new Paragraph("MONTH REVENUE: "+ month.getText().toString()));
            //doc.add(new Paragraph("Customer ID: " + makhachhang));
            doc.add(new Paragraph("DATE STATISTIC: " + formatter.format(now)+""));
            
            PdfPTable tbl = new PdfPTable(5);
            tbl.setSpacingBefore(11f);
            tbl.setSpacingAfter(11f);
            
            tbl.addCell("MATT");
            tbl.addCell("MAKH");
            tbl.addCell("MAPHONG");
            tbl.addCell("NGAYTT");
            tbl.addCell("THANHTIEN");

            for (int i=0;i<DoanhThutb.getRowCount();i++)
            {
                String matt = DoanhThutb.getModel().getValueAt(i, 0).toString();
                String makh = DoanhThutb.getModel().getValueAt(i, 1).toString();
                String maphong = DoanhThutb.getModel().getValueAt(i, 2).toString();
                String ngtt = DoanhThutb.getModel().getValueAt(i, 3).toString();
                String thanhtien = formatNumber(Integer.parseInt(DoanhThutb.getModel().getValueAt(i, 4).toString()))+ " VND";
                
                tbl.addCell(matt);
                tbl.addCell(makh);
                tbl.addCell(maphong);
                tbl.addCell(ngtt);
                tbl.addCell(thanhtien);
            }
            
            doc.add(tbl);
            doc.add(new Paragraph("REVENUE: " + formatNumber(Integer.parseInt(SumOf.getText().toString())) + " VND"));

        }
        catch(Exception e)
        {
            System.out.println(e);
            JOptionPane.showMessageDialog(null, "Lỗi in hóa đơn!");
        }
            
        doc.close();
    }//GEN-LAST:event_printMouseClicked

